#include<stdio.h>
#include<string>
#include<iostream>
#include<algorithm>
using namespace std;

int main() {
	int n; scanf("%d",&n);
	long long D[10][n]={0,};
	D[0][0] = 0; D[1][0] = 1;D[2][0] = 1;D[3][0] = 1;D[4][0] = 1;
	D[5][0] = 1; D[6][0] = 1;D[7][0] = 1;D[8][0] = 1;D[9][0] = 1;
	for(int i = 1; i<n; i++){
		D[0][i] = D[1][i-1]%1000000000;
		D[1][i] = (D[2][i-1]%1000000000+D[0][i-1]%1000000000)%1000000000;
		D[2][i] = (D[3][i-1]%1000000000+D[1][i-1]%1000000000)%1000000000;
		D[3][i] = (D[4][i-1]%1000000000+D[2][i-1]%1000000000)%1000000000;
		D[4][i] = (D[5][i-1]%1000000000+D[3][i-1]%1000000000)%1000000000;
		D[5][i] = (D[6][i-1]%1000000000+D[4][i-1]%1000000000)%1000000000;
		D[6][i] = (D[7][i-1]%1000000000+D[5][i-1]%1000000000)%1000000000;
		D[7][i] = (D[8][i-1]%1000000000+D[6][i-1]%1000000000)%1000000000;
		D[8][i] = (D[9][i-1]%1000000000+D[7][i-1]%1000000000)%1000000000;
		D[9][i] = D[8][i-1]%1000000000;
	}
	long long ans = (D[0][n-1]+D[1][n-1]+D[2][n-1]+D[3][n-1]+D[4][n-1]+D[5][n-1]+D[6][n-1]+D[7][n-1]+D[8][n-1]+D[9][n-1])%1000000000;
	printf("%lld",ans);
	return 0;
}
